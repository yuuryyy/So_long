#include "libft.h"

char	*ft_strdup(char *str)
{
	char	*dup;
	int		i;

	dup = (char *) malloc (ft_strlen(str) * sizeof(char) + 1);
	if (!dup)
		return (0);
	i = 0;
	while (str[i])
	{
		dup[i] = str[i];
		i++;
	}
	dup[i] = '\0';
	return (dup);
}
