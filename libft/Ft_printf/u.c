#include "ft_printf.h"

void	ft_putu(unsigned int n, int *len)
{
	if (n < 10)
		ft_putchar(n + '0', len);
	else
	{
		ft_putu(n / 10, len);
		ft_putu(n % 10, len);
	}
}
